    public static boolean writeEncryptionFile(String filePath, List<String> contentList, Cipher cipher, boolean append) {
        if (contentList.isEmpty()) {
            return false;
        }

        CipherOutputStream outputStream = null;

        try {
            makeDirs(filePath);
            outputStream = new CipherOutputStream(new FileOutputStream(filePath, append), cipher);
            int i = 0;
            for (String line : contentList) {
                if (i++ > 0) {
                    outputStream.write("\r\n".getBytes());
                }
                outputStream.write(line.getBytes());
            }
            return true;
        } catch (Exception e) {
            return false;
        } finally {
            if (outputStream != null) {
                try {
                    outputStream.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        }
    }


